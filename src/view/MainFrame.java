    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if (!updateStudent) {
            String id = txtCode.getText();
            String name = txtName.getText();
            String phone = txtPhone.getText();
            String email = txtEmail.getText();
            String courseName = txtCourseName.getText();
            String courseCode = txtCourseCode.getText().toUpperCase();

            if (!txtCredit.getText().matches("^[0-9]{1,2}$")) {
                lbCredit.setText("Credit is number");
            } else {
                lbCredit.setText(null);
            }
            int credit = Integer.parseInt(txtCredit.getText());

            if (!txtScore.getText().matches("^[0-9]{1,2}$")) {
                lbScore.setText("Score is number");
            } else {
                lbScore.setText(null);
            }
            int score = Integer.parseInt(txtScore.getText());

            Course course = new Course(courseCode, courseName);
            student = new Student(id, name, phone, email, course, credit, score);

        } else {
            for (int i = 0; i < students.size(); i++) {
                if (student.getId().equalsIgnoreCase(students.get(i).getId())) {
                    students.get(i).setName(txtName.getText());
                    students.get(i).setPhone(txtPhone.getText());
                    students.get(i).setEmail(txtEmail.getText());
                    String courseCode = txtCourseCode.getText().toUpperCase();
                    String courseName = txtCourseName.getText();
                    students.get(i).setCourse(new Course(courseCode, courseName));
                    students.get(i).setCredit(Integer.parseInt(txtCredit.getText()));
                    students.get(i).setScore(Integer.parseInt(txtScore.getText()));
                }
            }
        }

        if (validate(student)) {
            if (!updateStudent) {
                students.add(student);
            }
            try {
                domParser.updateFile(students, "student.xml");
            } catch (ParserConfigurationException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            clearLabel();
            mouseClick = false;
            dialogFrame.setVisible(false);
            JOptionPane.showMessageDialog(this, "XML file update successfully");
            load();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

