    public void load() {
        stAXIterator = new StAX_Iterator_Parser();
        students = new Students();
        vctData = new Vector();
        stAXIterator.parseFile("student.xml");
        students = stAXIterator.getStudents();
        for (Student student : students) {
            Vector row = new Vector();
            row.add(student.getId());
            row.add(student.getName());
            row.add(student.getPhone());
            row.add(student.getEmail());
            row.add(student.getCourse().getCourseCode());
            row.add(student.getCourse().getCourseName());
            row.add(student.getCredit());
            row.add(student.getScore());
            vctData.add(row);
            tblStudent.setModel(new DefaultTableModel(vctData, vctHeader));
        }

    }

