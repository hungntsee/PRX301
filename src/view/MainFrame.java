    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!mouseClick) {
            JOptionPane.showMessageDialog(this, "Choose 1 student for deleting");
        } else {
            for (int i = 0; i < students.size(); i++) {
                if (student.getId().equalsIgnoreCase(students.get(i).getId())) {
                    students.remove(i);
                }
            }
            try {
                domParser.updateFile(students, "student.xml");
            } catch (ParserConfigurationException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            mouseClick = false;
            JOptionPane.showMessageDialog(this, "XML file update successfully");
            load();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

