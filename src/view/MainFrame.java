    public boolean validate(Student student) {

        //validateID
        if (student.getId() == null) {
            lbCode.setText("ID can't be empty.");
            return false;
        } else if (!student.getId().matches("^SE[0-9]{5}")) {
            this.txtCode.requestFocus();
            lbCode.setText("Wrong formatt ID [SE00000]");
            return false;
        } else if (!updateStudent) {
            for (int i = 0; i < students.size(); i++) {
                if (student.getId().equalsIgnoreCase(students.get(i).getId())) {
                    this.txtCode.requestFocus();
                    lbCode.setText("This ID already existed");
                    return false;
                } else {
                    lbCode.setText(null);
                }
            }
        } else {
            lbCode.setText(null);
        }

        //validate Name
        if (student.getName().length() < 5 || student.getName().length() > 30) {
            this.txtName.requestFocus();
            lbName.setText("Name has length from 5 to 30 "
                    + "\n" + "characters.");
            return false;
        } else if (student.getName().matches("/d")) {
            this.txtName.requestFocus();
            lbName.setText("Name only contains characters.");
            return false;
        } else {
            lbName.setText(null);
        }

        //validate phone
        if (student.getPhone().length() != 10) {
            this.txtPhone.requestFocus();
            lbPhone.setText("Phone must have 10 numbers");
            return false;
        } else if (!student.getPhone().matches("[0-9]{10}")) {
            this.txtPhone.requestFocus();
            lbPhone.setText("Phone must have 10 numbers");
            return false;
        } else {
            lbPhone.setText(null);
        }

        //validate email
        if (!student.getEmail().matches("^(.+)@(.+).(.+)$")) {
            this.txtEmail.requestFocus();
            lbEmail.setText("Email has formatt [aaa@aaa.aaa]");
            return false;
        } else {
            lbEmail.setText(null);
        }

        //validate courseCode
        if (!student.getCourse().getCourseCode().matches("^[A-Z]{3}+[0-9]{3}$")) {
            this.txtCourseCode.requestFocus();
            lbCourse.setText("Wrong formatt. Course Code has formatt [XXX000]");
            return false;
        } else {
            lbCourse.setText(null);
        }
        return true;
    }

    public void updateStudent(Student student) {
        txtCode.setText(student.getId());
        txtCode.setEditable(false);
        txtName.setText(student.getName());
        txtPhone.setText(student.getPhone());
        txtEmail.setText(student.getEmail());
        txtCourseCode.setText(student.getCourse().getCourseCode());
        txtCourseName.setText(student.getCourse().getCourseName());
        txtCredit.setText(String.valueOf(student.getCredit()));
        txtScore.setText(String.valueOf(student.getScore()));

    }

